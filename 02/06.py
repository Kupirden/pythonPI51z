print("Отвечай на вопросы 'да' или 'нет'.")

# Первый вопрос
answer1 = input("Любишь ли ты котиков? ")
if answer1 == "да":
  print("Класс! Я тоже!")
elif answer1 == "нет":
  print("Ну, у каждого свой вкус.")
else:
  print("Не понял, что ты сказал.  Играем дальше!")

# Второй вопрос
